function RenderMaze() {
    context.drawImage(
        image,
        132, 12,
        8, 8,
        0, 0,
        224, 288,
    );
    for (let y = 0; y < 36; y++) {
        for (let x = 0; x < 28; x++) {
            var p = GetSpritePos(mazes[curMaze][y][x]);
            context.drawImage(
                image,
                p.x, p.y,
                8, 8,
                x*8, y*8,
                8, 8,
            );
        }
    }
}
function RenderGhosts() {
    for (const c of ghosts){
        var p = GetSpritePos(32+c.type);
        context.drawImage(
            image,
            p.x, p.y,
            8, 8,
            c.x*8-4, c.y*8-4,
            8, 8,
        );
        context.drawImage(
            image,
            p.x+8, p.y,
            8, 8,
            c.x*8+4, c.y*8-4,
            8, 8,
        );
        context.drawImage(
            image,
            p.x, p.y+8,
            8, 8,
            c.x*8-4, c.y*8+4,
            8, 8,
        );
        context.drawImage(
            image,
            p.x+8, p.y+8,
            8, 8,
            c.x*8+4, c.y*8+4,
            8, 8,
        );
    }
}
function ComputeGhosts() {
    for (const c of ghosts){
        let o = [];
        if (c.state=="eaten" && (mazes[curMaze][c.y][c.x] == 31 || mazes[curMaze][c.y][c.x] == 16)){
            c.facing = 2;
            c.y = c.y+1;
            continue;
        }
        for (let d = 0; d < 4; d++){
            //console.log(c.facing + ", " +d);
            if (((c.facing+2)%4) == d) continue;
            let m = {x:0, y:0, f:d};
            if (d == 0) m.y = -1;
            else if (d == 1) m.x = -1;
            else if (d == 2) m.y = 1;
            else if (d == 3) m.x = 1;
            const w = mazes[curMaze][c.y+m.y][c.x+m.x]
            //console.log(w);
            if (!(w == 0 || w == undefined || (w == 31 && c.state=="eaten"))) continue;
            context.drawImage(
                image,
                128, 0,
                8, 8,
                (m.x+c.x)*8, (m.y+c.y)*8,
                8, 8,
            );
            o[d] = m;
        }
        const t = GetGhostTarget(c.type, c.state);
        context.beginPath();
        context.moveTo(c.x*8+4, c.y*8+4);
        context.lineTo(t.x*8+4, t.y*8+4);
        context.strokeStyle = t.c;
        context.stroke();
        let b = {x:0, y:0, f:0, d:10000000};
        for (let d = 0; d < 4; d++){
            if (o[d] == undefined) continue;
            m = o[d];
            m.d = (c.x+m.x-t.x)*(c.x+m.x-t.x) + (c.y+m.y-t.y)*(c.y+m.y-t.y);
            if (c.state == "frightened") m.d = Math.random()*100;
            //console.log(m.f + ", " + m.x + ", " + m.y + ", " + m.d);
            if (m.d < b.d){
                b = m;
                //console.log(m.f + ", " + m.x + ", " + m.y + ", " + m.d);
            }
        }
        context.drawImage(
            image,
            144, 0,
            8, 8,
            (b.x+c.x)*8, (b.y+c.y)*8,
            8, 8,
        );
        //console.log("Moved: "+c.x + ", "+c.y)
        c.x = (c.x + b.x+28)%28;
        c.y = c.y + b.y;
        c.facing = b.f;
    }
}